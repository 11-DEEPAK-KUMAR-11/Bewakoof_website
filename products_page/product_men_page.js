function displayData(data) {
  document.querySelector("#container").innerHTML = "";
  fashionData.forEach(function (elem) {
    //console.log (elem.name)
    var box = document.createElement("div");
    var fashion_avatar = document.createElement("img");
    fashion_avatar.setAttribute("src", elem.avatar);
    fashion_avatar.style.cursor = "pointer";
    var fashion_brand = document.createElement("h3");
    fashion_brand.innerText = elem.brand;
    var fashion_name = document.createElement("p");
    fashion_name.innerText = elem.name;

    var div5 = document.createElement("div");
    var fashion_rupees = document.createElement("h3");
    fashion_rupees.innerText = elem.rupees;
    fashion_rupees.style.color = "black";
    div5.append(fashion_rupees)

    var div1 = document.createElement("div");
    var fashion_price = document.createElement("h3");
    fashion_price.innerText = elem.price;
    fashion_price.style.color = "black";
    div1.append(fashion_price)

    var div6 = document.createElement("div");
    var fashion_rupees2 = document.createElement("h4");
    fashion_rupees2.innerText = elem.rupees2;
    div6.append(fashion_rupees2)

    var fashion_price2 = document.createElement("h4");
    fashion_price2.innerText = elem.price2;
    var div2 = document.createElement("div");
    div2.append(fashion_price2)

    var div3 = document.createElement("div");
    div3.append(div5, div1, div6, div2);

    var fashion_offer = document.createElement("h4");
    fashion_offer.innerText = elem.offer;

    fashion_avatar.addEventListener("click", function () {
      press(elem);
    })
    box.append(fashion_avatar, fashion_brand, fashion_name, div3, fashion_offer);
    document.querySelector("#container").append(box);
    //console.log (elem)

  })
}
